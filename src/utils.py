def to_numpy(tensor):
    if torch.is_tensor(tensor):
        return tensor.cpu().numpy()
    elif type(tensor).__module__ != "numpy":
        raise ValueError("Cannot convert {} to numpy array".format(type(tensor)))
    return tensor


def to_torch(ndarray):
    if type(ndarray).__module__ == "numpy":
        return torch.from_numpy(ndarray)
    elif not torch.is_tensor(ndarray):
        raise ValueError("Cannot convert {} to torch tensor".format(type(ndarray)))
    return ndarray


def set_and_check_load_epoch(args):
    models_dir = args.output_dir
    if os.path.exists(models_dir):
        if args.load_epoch == -1: # find the latest one
            load_epoch = -1
            for file in os.listdir(models_dir):
                if file.startswith(f'model_{args.model_id}_epoch_'):
                    load_epoch = max(load_epoch, int(file.split('_')[-1][:-4]))
            args.load_epoch = load_epoch
        else:
            found = False
            for file in os.listdir(models_dir):
                if file.startswith(f'model_{args.model_id}_epoch_'):
                    found = int(file.split('_')[-1][:-4]) == args.load_epoch
                    if found: break
            assert found, 'Model for epoch %i not found' % args.load_epoch
    else:
        assert args.load_epoch < 1, 'Model for epoch %i not found' % args.load_epoch
        args.load_epoch = -1

